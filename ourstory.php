    <h2>Our Story</h2>
</div>
<div class="ourstory">
    <p>It all started when we met as kids in December of 1998. Our parents were good friends so we spent quite a bit of time together, 
but little did we know that 17 years later we would fall in love and 18 years later we would get married.</p>
 
<p>It wasn’t until summer of 2014 until we started talking (or whatever kids call it these days). 
We went on our first date on August 11th. We went miniature golfing, raced Go-carts, and ate delicious food at Chuy’s.
We talked at the restaurant until 10pm, and neither one of us wanted that night to end.</p>
 
<p>On November 22nd, we had a fantastic night filled with Nashville Predators hockey and <em>Scott Pilgrim vs. the World</em>. 
On this night, we decided to make our relationship official. We didn’t want that night to end either.</p>
 
<p>We had many wonderful adventures after that day, including kayaking (only one snake related incident), trips to Texas and Arkansas, a cruise to Cancun, 
fun times with friends and family, and an amazing trip to Europe (filled with stories we’ll gladly tell), and many more. 
All the while, we were falling deeper and deeper in love with each other.</p>
 
<p>All of those adventures led up to one day in particular. A day that would change our lives forever. 
A day in which we would realize that our greatest adventures are yet to come. 
A day where after weeks of meticulous planning, a certain question was asked, and a certain answer was given. 
That day was December 19th, 2015 - the day we got engaged.</p>
</div>

